function Hamiltonian(du, u, p, t)
    x1 = u[1]
	y1 = u[2]
  	p1x = u[3]
	p1y = u[4]
	x2 = u[5]
    y2 = u[6]
	p2x = u[7]
	p2y = u[8]

   	m1 = p[1]
    m2 = p[2]
	G = p[3]

	difX, difY = x2 - x1, y2 - y1
	r = (((difX ^ 2) + (difY ^ 2)) ^ 1.5)
	top = G * m1 * m2

	du[1], du[2] = p1x / m1, p1y / m1
	du[3], du[4] = top * difX / r, top * difY / r

	du[5], du[6] = p2x / m2, p2y / m2
	du[7], du[8] = - top * difX / r, - top * difY / r
end

